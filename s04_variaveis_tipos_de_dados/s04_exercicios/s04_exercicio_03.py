"""
03) Peça ao usuário para digitar três valores inteiros e imprima a soma deles.
"""

n1, n2, n3 = int(input('Digite o número 1: \n')), \
             int(input('Digite o número 2: \n')), \
             int(input('Digite o número 3: \n'))

print(f'A soma dos números digitados é: {n1 + n2 + n3} \n')

